package com.example.liquibase.service.DTO.mapper;

import com.example.liquibase.domain.User;
import com.example.liquibase.service.DTO.LoginDTO;
import org.mapstruct.Mapper;
import org.mapstruct.factory.Mappers;
import org.springframework.stereotype.Component;

@Component
public class LoginDTOToUserMapper {
    public User map(LoginDTO loginDTO) {
        User user = new User();
        user.setEmail(loginDTO.getEmail());
        return user;
    }
}